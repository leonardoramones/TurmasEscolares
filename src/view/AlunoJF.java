    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        jButton1.setVisible(false);
        jButton3.setText("Confirmar");
        jButton4.setText("Cancelar");
        jTextField2.setEnabled(false);

        int linha = jTable1.getSelectedRow();
        String matricula = (String) jTable1.getValueAt(linha, 0);
        AlunoServicos alunoS = ServicosFactory.getAlunoServicos();
        Aluno a = alunoS.getAlunoByDoc(matricula);
        jTextField1.setText(a.getNome());
        jTextField2.setText(a.getMatricula());
        jTextField4.setText(a.getTurma());
        jTextField3.setText(a.getProjeto());
      
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        int linha = jTable1.getSelectedRow();
        String matricula = (String) jTable1.getValueAt(linha, 0);
        AlunoServicos alunoS = ServicosFactory.getAlunoServicos();
        String nome = alunoS.getAlunoByDoc(matricula).getNome();
        Object[] btnMSG = {"Sim", "Não"};
        int resp = JOptionPane.showOptionDialog(this,
                "Deseja realmente deletar " + nome, ".: Deletar :.",
                JOptionPane.DEFAULT_OPTION, JOptionPane.WARNING_MESSAGE,
                null, btnMSG, btnMSG[0]);
        if (resp == 0) {
            alunoS.deletarAluno(matricula);
            addRowToTable();
            JOptionPane.showMessageDialog(this, "Aluno deletado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Ok, delete cancelado pelo usuário!");
        }
        jButton4.doClick();
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        if (jButton4.getText().equals("Limpar")) {
            limparCampos();
        } else {
            jButton2.setEnabled(false);
            jButton4.setText("Limpar");
            jButton3.setText("Salvar");
            limparCampos();
        
    }  
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if (validaInputs()) {
            String matricula = jTextField2.getText();
            String nome = jTextField1.getText().toUpperCase();
            String turma = jTextField4.getText();
            String projeto = jTextField3.getText().toUpperCase();

            AlunoServicos alunoS = ServicosFactory.getAlunoServicos();
            Aluno a = new Aluno(0, nome, matricula, turma, projeto);
            if (jButton3.getText().equals("Salvar")) {
                alunoS.cadastroAluno(a);
            } else {
                alunoS.atualizarAluno(a);
            }
            addRowToTable();
            limparCampos();
        }
    
    }//GEN-LAST:event_jButton3ActionPerformed

