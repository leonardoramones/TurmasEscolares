    public void addRowToTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.getDataVector().removeAllElements();//remove todas linhas
        model.fireTableDataChanged();
        Object rowData[] = new Object[4];
        AlunoServicos alunoS = ServicosFactory.getAlunoServicos();
        for (Aluno aluno : alunoS.getAlunos()) {
            rowData[0] = aluno.getMatricula();
            rowData[1] = aluno.getNome();
            rowData[2] = aluno.getTurma();
            rowData[3] = aluno.getProjeto();
            model.addRow(rowData);
        }
    }
    
    public boolean validaInputs() {
        if (jButton2.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencher matricula!");
            jButton2.requestFocus();
            return false;
        } else if (jButton1.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencher nome!");
            jButton1.requestFocus();
            return false;
        } else if (jButton3.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencher turma!");
            jButton3.requestFocus();
            return false;
        } else if (jButton4.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Preencher projeto!");
            jButton4.requestFocus();
            return false;
        }
        return true;
    }

